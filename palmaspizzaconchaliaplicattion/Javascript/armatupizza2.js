    function agregarIng(valor) {
      var input = document.getElementById("2-input-ingredientes");
      if (input.value.includes(valor)) {
        input.value = input.value.replace(valor + ", ", "");
      } else {
        input.value += valor + ", ";
      }
    }

 

    
    

    
